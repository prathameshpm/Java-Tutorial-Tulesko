public class namingConventions {
    public static void main(String[] args) {
        System.out.println("We use camel casing");
        System.out.println("class and interface: Calc, Runable, MyData");
        System.out.println("variable and method: marks, show(), showMyMarks()");
        System.out.println("constants: PIE, BRAND, MY_DATA");
        System.out.println("Constructor: Calc(), Runable()");
    }
}
